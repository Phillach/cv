window.addEventListener('hashchange', function(event){
    var modalSkills = document.querySelector('.modal-skills');
    var isOpen = document.querySelector('.is-open');
    var skillsLink = document.querySelector('.skills-link');
    var currentLink = document.querySelector('.current-link');
    var closeBtn = document.querySelector('.close-btn');

    if (currentLink) {
        currentLink.classList.remove('current-link');
    }

    if (isOpen) {
        isOpen.classList.remove('is-open');
    }

    switch (window.location.hash) {
        case "#skills":
            modalSkills.classList.add('is-open');
            skillsLink.classList.add('current-link');

            break;
    }
});
